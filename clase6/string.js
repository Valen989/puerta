// .length ->  Retorna la cantidad total de caracteres del string, incluidos los espacios.

let nombre = "Eric";

// cuantos caracteres tiene mi nombre?

console.log(nombre.length);

// y si quiero mostrar solo a la inicial?
console.log(nombre[0]);


