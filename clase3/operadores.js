let valor1 = 15;
let valor2 = 15;

let suma = 10 + 15; // Suma → 25
console.log(suma);
let resta = 10 - 15; // Resta → -5
console.log(resta);
let multiplicacion = 10 * 15; // Multiplicación → 150
console.log(multiplicacion);
let division = 15 / 10; // División → 1.5
console.log(division);
let incremento = ++valor1; // Incremento, es igual a 15 + 1 → 16
console.log(incremento);
let decremento = --valor2; // Decremento, es igual a 15 - 1 → 14
console.log(decremento);
let modulo1 = 15 % 5; // Módulo, el resto de dividir 15 entre 5 → 0
console.log(modulo1);
let modulo2 = 15 % 2; // Módulo, el resto de dividir 15 entre 2 → 1
console.log(modulo2);

